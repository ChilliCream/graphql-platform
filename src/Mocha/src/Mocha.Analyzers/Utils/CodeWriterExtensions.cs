namespace Mocha.Analyzers.Utils;

/// <summary>
/// Provides extension methods for <see cref="CodeWriter"/> to emit common generated code patterns.
/// </summary>
public static class CodeWriterExtensions
{
    /// <summary>
    /// Writes a <see cref="System.CodeDom.Compiler.GeneratedCodeAttribute"/> annotation to the output.
    /// </summary>
    /// <param name="writer">The code writer to write to.</param>
    /// <exception cref="ArgumentNullException"><paramref name="writer"/> is <see langword="null"/>.</exception>
    public static void WriteGeneratedAttribute(this CodeWriter writer)
    {
        if (writer is null)
        {
            throw new ArgumentNullException(nameof(writer));
        }

#if DEBUG
        writer.WriteIndentedLine(
            "[global::System.CodeDom.Compiler.GeneratedCode(\"Mocha.Analyzers\", \"1.0.0\")]");
#else
        var version = typeof(CodeWriter).Assembly.GetName().Version!.ToString();
        writer.WriteIndentedLine(
            $"[global::System.CodeDom.Compiler.GeneratedCode(\"Mocha.Analyzers\", \"{version}\")]");
#endif
    }

    /// <summary>
    /// Writes the standard auto-generated file header including nullable enable and pragma directives.
    /// </summary>
    /// <param name="writer">The code writer to write to.</param>
    /// <exception cref="ArgumentNullException"><paramref name="writer"/> is <see langword="null"/>.</exception>
    public static void WriteFileHeader(this CodeWriter writer)
    {
        if (writer is null)
        {
            throw new ArgumentNullException(nameof(writer));
        }

        writer.WriteIndentedLine("// <auto-generated/>");
        writer.WriteLine();
        writer.WriteIndentedLine("#nullable enable");
        writer.WriteIndentedLine("#pragma warning disable");
        writer.WriteLine();
    }
}
